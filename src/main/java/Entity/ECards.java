package Entity;

public class ECards {
    private int totalClientes;
    private float totalSaldo;
    private int totalClienteTelf;

    public int getTotalClientes() {
        return totalClientes;
    }

    public void setTotalClientes(int totalClientes) {
        this.totalClientes = totalClientes;
    }

    public float getTotalSaldo() {
        return totalSaldo;
    }

    public void setTotalSaldo(float totalSaldo) {
        this.totalSaldo = totalSaldo;
    }

    public int getTotalClienteTelf() {
        return totalClienteTelf;
    }

    public void setTotalClienteTelf(int totalClienteTelf) {
        this.totalClienteTelf = totalClienteTelf;
    }   
}
